<?php
// Задание 3. Работа с операторами организации циклов (вариант 25)
// Вывести Фамилию и Имя n + 5 раз, где n — номер варианта (11). Использовать цикл while.

$surname = 'Родак';
$name    = 'Марина';
$n       = 11;
$times   = $n + 5; // 16 раз

$i = 0;
while ($i < $times) {
    $index = $i + 1;
    echo "{$index}. {$surname} {$name}<br>";
    $i++;
}
?>