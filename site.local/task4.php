<?php
// Задание 5. Работа со строками (вариант 25)
// s1 = ВАША ФАМИЛИЯ, s2 = АДРЕС.
// 1. Определить длину строки s1.
// 2. Выполнить сцепление строк s1 и s2.
// 3. Преобразовать строку s2 в верхний регистр.

$s1 = 'РОДАК';
$s2 = 'г. Гродно, ул. Новооктябрьская, д. 14';

echo "<strong>s1 (Фамилия):</strong> {$s1}<br>";
echo "<strong>s2 (Адрес):</strong> {$s2}<br>";

$lengthS1 = mb_strlen($s1, 'UTF-8');
echo "Длина строки s1: {$lengthS1} символов<br>";

$concatenated = $s1 . ' — ' . $s2;
echo "Сцепление s1 и s2: {$concatenated}<br>";

$s2Upper = mb_strtoupper($s2, 'UTF-8');
echo "Строка s2 в верхнем регистре: {$s2Upper}<br>";
?>