<?php
// Задание 4. Работа с массивами (вариант 25)
// 1. Сформировать массив из 6 целых элементов.
// 2. Найти сумму чётных элементов массива.
// 3. Выполнить сортировку элементов массива по убыванию.
// 4. Вывести исходный и изменённый массивы.

$numbers = [5, 12, -3, 8, 7, 10];

echo "<strong>Исходный массив:</strong> " . implode(', ', $numbers) . "<br>";

$evenSum = 0;
foreach ($numbers as $num) {
    if ($num % 2 === 0) {
        $evenSum += $num;
    }
}

echo "Сумма чётных элементов массива: {$evenSum}<br>";

rsort($numbers);
echo "<strong>Отсортированный по убыванию массив:</strong> " . implode(', ', $numbers) . "<br>";
?>