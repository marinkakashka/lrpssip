<?php
echo "<h2>3. Работа с операторами организации циклов</h2>";
echo "<p>Задание: Вывести фамилию и имя n+5 раз (n = 11), используя цикл do..while</p>";
$n = 11;
$count = $n + 5; // 11 + 5 = 16 раз
$i = 1;

echo "<p>Фамилия и имя (вариант 11, n+5 = $count раз):</p>";
echo "<ul>";
do {
    echo "<li>Родак Марина (вывод №$i из $count)</li>";
    $i++;
} while ($i <= $count);
echo "</ul>";
echo "<p><strong>Проверка:</strong> Фамилия выведена <strong>" . ($i-1) . "</strong> раз(а), что соответствует n+5 = $count</p>";
?>